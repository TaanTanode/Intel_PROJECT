    """)
    st.code("""
    svr = SVR(kernel='rbf', C=100, epsilon=0.1)
    svr.fit(X_train_scaled, y_train)
    """)

    st.subheader("7. ‡∏ó‡∏≥‡∏ô‡∏≤‡∏¢‡∏£‡∏≤‡∏Ñ‡∏≤‡∏´‡∏∏‡πâ‡∏ô‡∏ö‡∏ô‡∏ä‡∏∏‡∏î‡∏ó‡∏î‡∏™‡∏≠‡∏ö")
    st.write("‡πÄ‡∏°‡∏∑‡πà‡∏≠‡∏ô‡∏≥‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• X_test_scaled ‡πÑ‡∏õ‡∏ú‡πà‡∏≤‡∏ô‡πÇ‡∏°‡πÄ‡∏î‡∏• SVR ‡∏à‡∏∞‡πÑ‡∏î‡πâ‡∏Ñ‡πà‡∏≤‡∏ó‡∏≥‡∏ô‡∏≤‡∏¢ (y_pred)")
    st.code("""
